#[derive(Clone, Debug, PartialEq, Eq)]
pub enum TokenType {
    Declaration,
    Write,
    InputString,
    InputNumber,
    LeftBrace,
    RightBrace,
    If,
    Else,
    Loop,
    Assignment,
    Plus,
    Minus,
    Product,
    Divide,
    Modulus,
    GreaterThan,
    LessThan,
    EqualTo,
    NotEqual,
    SemiColon,
    OpenParantheses,
    CloseParantheses,
    Literal(String),
    Number(i64),
    Symbol(String),
}